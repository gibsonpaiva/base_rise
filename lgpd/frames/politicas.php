            <div class="col-md-12" table-responsive">
	<h3><br><b>C</b>ONCEITOS & <b>R</b>EGRAS</h3>
                <hr> 
			<table class="table table-hover table-bordered table-responsive">
		   <tr><th><b> C01</b></th><td> A Lei Geral de Proteção de Dados Pessoais (LGPD ou LGPDP), Lei nº 13.709/2018[1], é a legislação brasileira que regula as atividades de tratamento de dados pessoais e que também altera os artigos 7º e 16 do Marco Civil da Internet.</td></tr>
                    <tr><th><b> C02</b></th><td>A norma foi aprovada em 2018 e estabelece regras para o uso, coleta, armazenamento e compartilhamento de dados dos usuários por empresas públicas e privadas.</td></tr>
                    <tr><th><b> C03</b></th><td> Ela pretende garantir mais segurança, privacidade e transptarência no uso de informações pessoais.</td></tr>
                    <tr><th><b> C04</b></th><td>Foi inspirada pelo Regulamento Geral de Proteção de Dados da União Européia (GDPR), e tem por objeitvo proteger dados pessoais de uso indevido. </td></tr>
                    <tr><th><b> C05</b></th><td> As empresas precisam informar aos seus usuários quais dados estão coletando e o que farão com eles.</td></tr>
                    <tr><th><b> C06</b></th><td> Os dados considerados como "sensíveis" são: <br><br> 
			- Origem racial ou étnica;<br>
			- Filiação à organizações políticas ou religiosas;<br>
			- Filiação sindical;<br>
			- Informações genéticas e de biometria;<br> 
			- Orientação sexual.</td></tr>
                    <tr><th><b> R01</b></th><td> Não é permitida a divulgação dos documentos contidos nessa seção.</td></tr>
                    <tr><th><b> R02</b></th><td> Se encontrar alguma falha, gentileza reportar ao Encarregado de Dados (dpo@itone.com.br).</td></tr>
                    <tr><th><b> R03</b></th><td> Em caso de dúvidas, procure o seu gestor imediato.</td></tr>
                    <tr><th><b> R04</b></th><td> Para mais informações recomentamos acessar o site do LGPD.</td></tr>
                </table>
