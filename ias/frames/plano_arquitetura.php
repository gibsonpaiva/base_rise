                <h3><a name="PAMontagem"></a> MONTAGEM </h3><br>
                
                <table width="100%"> 
                    
                    <tr><th><b> 1</b></th><td> Antes de começar qualquer trabalho de Arquitetura de Implementação, você vai precisar instalar as FONTES utilizadas nos documentos e no portal. Não continue, até completar esse passo.​</td></tr>
                    <tr><th><b> 2</b></th><td> Baixe as últimas versões dos MODELOS e mude o nome dos arquivos.</td></tr>
                    <tr><th><b> 3</b></th><td> <b>PLANO DE ARQUITETURA - CAPA</b>

                        <br> - Ao abrir o documento pela primeira vez, é necessário preencher dinamicamente a capa. 
                        <br> - O procedimento descreve como preencher o METADADOS e está descrito abaixo:
                        <br> - Abra o(s) arquivo(s) MODELO(s) do Plano de Arquitetura
                        <br> - Clique em "File" + "Info" + "Properties" + "Advanced Properties"
                        <br> - Será exibido na próxima tela, o metadados do arquivo
                        <br> - Preencha os metadados conforme INSTRUÇÕES e o VÍDEO.
                        <br> - Ao terminar, verifique ao final se a capa foi preenchida dinamicamente.
                        <br> - Se autorizado pelo cliente, insira no final da página o LOGOTIPO dele.
                        <br> - Abaixo, resultado do preenchimento dinâmico da capa pelo metadados. 
                    
                    </td></tr>

                    <tr><th><b> 4</b></th><td> <b>PLANO DE ARQUITETURA - RESPONSABILIDADES</b>
                        
                        <br> Localize no documento, a pasta INICIAÇÃO, e preencha as informações das RESPONSABILIDADES do projeto.
                    
                    </td></tr>

                    <tr><th><b> 5</b></th><td> <b>PLANO DE ARQUITETURA - PLANEJAMENTO</b>
                        
                        <br> Localize no documento, a pasta PLANEJAMENTO, e comece o trabalho de arquitetura nas páginas seguintes.
                    
                    </td></tr>

                    <tr><th><b> 6</b></th><td> <b>PLANO DE ARQUITETURA - ASSUNTOS</b>
                        
                        <br> Verifique em PADRÕES ( Principais assuntos que um PLANO DE ARQUITETURA deve abranger ) e comece a montagem.
                    
                    </td></tr>

                    <tr><th><b> 7</b></th><td> <b>APRESENTAÇÃO DO PA PARA O GP E IE</b>
                        
                        <br> Ao término do Plano de Arquitetura, marque uma reunião e faça a apresentação para o Gerente de Projetos e Implementador. 
                        <br> Havendo necessidades de mudanças, faça-as e apresente o plano novamente. 
                        <br> Solicite o "de acordo" e registre depois da pasta CONTROLE.
                    
                    </td></tr>

                    <tr><th><b> 8</b></th><td> <b>APRESENTAÇÃO DO PA PARA O CLIENTE</b>
                        
                        <br> Depois da apresentação para o Gerente de Projetos e Implementador, marque nova reunião para apresentar o Plano de Arquitetura para o CLIENTE. 
                        <br> Havendo necessidades de mudanças, faça-as e apresente o plano novamente. 
                        <br> Solicite o "de acordo" e registre depois da pasta CONTROLE.
                    
                    </td></tr>

                    <tr><th><b> 9</b></th><td> <b>EVIDÊNCIAS DE EXECUÇÃO E DOCUMENTAÇÃO</b>
                        
                        <br> Depois da execução, colete as evidências de instalação com o implementador.
                        <br> Adicione à pasta EXECUÇÃO do Plano de Arquitetura
                        <br> Mude o nome do documento para DOCUMENTAÇÃO.
                    
                    </td></tr>
